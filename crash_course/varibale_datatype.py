"""
String Data Type
"""
name = "Sabbir"  # string
fndName = 'Rafi'  # string str

print(type(name))  # output: str
print(fndName)

"""
Number Data Type
"""
age = 20
print(age)

"""
list Data Type
"""
rolls = [20, 30, 40, 50]  # list
print(rolls)

"""
dict Data Type
"""
fnds = {"Tahsin", "Rafi", "Tanvir", "Ankon"}
print(fnds)

"""
Bool Data Type
"""
isOpen = False | True
print(isOpen)
